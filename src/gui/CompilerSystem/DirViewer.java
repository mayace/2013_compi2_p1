  private void startDirw(Path dir) throws HeadlessException {
    if (fexists(dir) && Files.isDirectory(dir)) {
      if (dirw.isAlive()) {
        dirw.interrupt();
      }
      dirw = new DirWatcher(dir, getFilter(), list.getModel(), dirw.getObservers());
      dirw.start();
    }

  }

